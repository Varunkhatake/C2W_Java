// Unary operator

class unary{
	public static void main(String[] args){
		int x= 10;
		System.out.println(-x);
		System.out.println(+x);
		//pre increment
		System.out.println(++x);
		// pre decrement
		System.out.println(--x);

		int s = 5;
		System.out.println(s++);
		System.out.println(s);
		int y = 8;
		System.out.println(y--);
		System.out.println(y);
	}
}
