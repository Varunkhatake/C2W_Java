class C14{
        public static void main(String[] ar){
                int num = 7;
                System.out.println("before switch");
                switch(num){
                        case 1:
                                System.out.println("1");
                                break;
                        case 2:
                                System.out.println("2");
                                break;
                        case 3:
                                System.out.println("3");
                                break;
                        default:
                                System.out.println("default");

                        }
                System.out.println("after switch");
        }
}
